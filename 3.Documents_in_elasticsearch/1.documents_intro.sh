How the logs are getting stored in elasticsearch

Everything stored in elasticseach is in the form of Documents.

Documents:
1. They are the fundamental unit of data that gets stored in elasticsearch
2. Documents are structured in a JSON format
3. Optimized for real-time search
4. indexing - when data is stored in index .. elasticseach converts it an inverted index.
5. Node
6. Sharding and Replicas
7. Schema-less nature of elasticseach

All above features enable elasticseach for large scale and complex data analysis

